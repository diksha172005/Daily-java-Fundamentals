public class PredefinedMethods {
    // Already defined in class library and are also known as Standard library methods or Built-in methods.
    public static void main(String[] args) {
        Math.random(); // Returns random values
        double power = Math.pow(2,3); // Returns 2 raised to power 3
        double maxVal = Math.max(10,20);// Returns maximum of 10 and 20

        System.out.println(Math.random());
        System.out.println("2 raised to 3 is: " +power);
        System.out.println("Max of 10 and 20 is: " + maxVal);
    }
       
}
