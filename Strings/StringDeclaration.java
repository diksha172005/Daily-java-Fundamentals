package Strings;

public class StringDeclaration {
    public static void main(String[] args) {
        // Declaring String without using "new" operator
        String name =  "String_1";
        System.out.println("String name = " + name);

        // Declaring String using "new" operator
        String NewName = new String("String_2");
        System.out.println("New String = " +NewName);
    }
}
